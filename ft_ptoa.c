#include"ft_printf.h"

char	*ft_ptoa(uintptr_t p)
{
	char		*str;
	size_t		len;
	uintptr_t	num;

	num = p;
	len = 0;
	while (p != 0)
	{
		p /= 16;
		len++;
	}
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (str == NULL)
		return (NULL);
	str[len] = '\0';
	while (len > 0)
	{
		str[len - 1] = ft_xtoc(num % 16, 0);
		num /= 16;
		len--;
	}
	return (str);
}
